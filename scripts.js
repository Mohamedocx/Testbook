const quizContainer = document.getElementById('question-container');
const submitBtn = document.getElementById('submit-btn');
const resultsContainer = document.getElementById('results-container');

function loadQuiz() {
    quizData.forEach((item, index) => {
        const questionElement = document.createElement('div');
        questionElement.classList.add('question');
        
        const questionTitle = document.createElement('h3');
        questionTitle.innerText = item.question;
        questionElement.appendChild(questionTitle);
        
        const optionsContainer = document.createElement('div');
        optionsContainer.classList.add('options');
        
        item.options.forEach((option, optIndex) => {
            const optionElement = document.createElement('label');
            optionElement.innerHTML = `
                <input type="radio" name="question${index}" value="${optIndex}">
                ${option}
            `;
            optionsContainer.appendChild(optionElement);
        });
        
        questionElement.appendChild(optionsContainer);
        quizContainer.appendChild(questionElement);
    });
}

function showResults() {
    let score = 0;
    
    quizData.forEach((item, index) => {
        const selectedOption = document.querySelector(`input[name="question${index}"]:checked`);
        const correctOption = document.querySelector(`input[name="question${index}"][value="${item.correct}"]`).parentElement;

        if (selectedOption && parseInt(selectedOption.value) === item.correct) {
            score++;
        }

        correctOption.style.color = 'green';
    });
    
    resultsContainer.innerHTML = `لقد حصلت على ${score} من ${quizData.length}`;
}

submitBtn.addEventListener('click', showResults);

loadQuiz();